    public static void main(String args[]) {
     SplashScreen sp = new SplashScreen();
     sp.setVisible(true);
     
     
      try {
          
          for(int i=0;i<=100;i++){
              Thread.sleep(40);
              sp.LoadingValue.setText(i +"%");
              
              if(i==10){
                  sp.LoadingLabel.setText("Memulai aplikasi..");
              }
              if(i==20){
                  sp.LoadingLabel.setText("Memuat data yang dibutuhkan...");
              }
              if(i==30){
                  sp.LoadingLabel.setText("Mengumpulkan sumber data");
              }
              if(i==40){
                 sp.LoadingLabel.setText("Menginstalasi data");
              }
              if(i==50){
                  sp.LoadingLabel.setText("Koneksi ke database...");
              }
              if(i==70){
                  sp.LoadingLabel.setText("Sukses Mengkoneksi...");
              }
              if(i==80){
                  sp.LoadingLabel.setText("Menjalankan Cashier...");
              }
              sp.LoadingBar.setValue(i);
          }
      } catch (Exception e) {
          JOptionPane.showMessageDialog(null, e);
      }
        sp.setVisible(false);
        sp.dispose(); 
        new ABSframe().setVisible(true);
    }
    


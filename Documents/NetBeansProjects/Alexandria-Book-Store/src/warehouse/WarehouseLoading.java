   private class LoadingTask extends SwingWorker<Void, Integer> {
        @Override
        protected Void doInBackground() throws Exception {
            for (int i = 0; i <= 100; i++) {
                Thread.sleep(40);
                publish(i);
                
                switch (i) {
                    case 10 -> LoadingLabel.setText("Memulai aplikasi..");
                    case 20 -> LoadingLabel.setText("Memuat data...");
                    case 30 -> LoadingLabel.setText("Mengumpulkan sumber data");
                    case 40 -> LoadingLabel.setText("Menginstalasi data");
                    case 50 -> LoadingLabel.setText("Koneksi ke database...");
                    case 70 -> LoadingLabel.setText("Sukses Mengkoneksi...");
                    case 80 -> LoadingLabel.setText("Menjalankan Warehouse...");
                }
            }
            return null;
        }

        @Override
        protected void process(java.util.List<Integer> chunks) {
            int value = chunks.get(chunks.size() - 1);
            LoadingValue.setText(value + "%");
            LoadingBar.setValue(value);
        }

        @Override
        protected void done() {
            try {
                // Ensure the loading task completed successfully
                get();
                // Proceed to the login screen
                new Login().setVisible(true);
                WarehouseLoading.this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(WarehouseLoading.this, e);
            }
        }
    }    
    
